void	ft_putchar(char c);

void	ft_putnbr(int n)
{
	char	tmp[12];
	int		i;

	i = 0;
	if (n < 0)
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putchar('2');
			n = -147483648;
		}
		n = -n;
	}
	while (n > 9)
	{
		tmp[i++] = (n % 10) + 48;
		n = n / 10;
	}
	if (n < 10)
		tmp[i] = n + 48;
	while (i >= 0)
		ft_putchar(tmp[i--]);
}
