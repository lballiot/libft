#include "libft.h"

int		ft_isalnum(int c)
{
	if ((('0' <= c) && ('9' >= c)) || (('A' <= c) && ('Z' >= c)) || (('a' <= c)
					&& ('z' >= c)))
		return (1);
	return (0);
}
